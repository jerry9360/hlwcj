package com.centit.hlwyw.inner.entity;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

import com.centit.hlwyw.core.entity.BaseEntity;

@Entity
@Table(name = "corpinfo")
public class Corpinfo extends BaseEntity {
	

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	@Id
	@Column(name = "corp_id")	
    private String corp_id;
	@Column(name = "credit_code")	
    private String credit_code;
	@Column(name = "industry")	
    private String industry;
	@Column(name = "corp_name")	
    private String corp_name;
	@Column(name = "corp_short")	
    private String corp_short;
	@Column(name = "istag")
	private String istag;
	@Column(name = "create_date")	
    private Date create_date;
	@Column(name = "modify_date")	
    private Date modify_date;

	public String getCorp_id() {
		return corp_id;
	}

	public void setCorp_id(String corp_id) {
		this.corp_id = corp_id;
	}
	
	public String getCredit_code() {
		return credit_code;
	}

	public void setCredit_code(String credit_code) {
		this.credit_code = credit_code;
	}

	public String getIndustry() {
		return industry;
	}

	public void setIndustry(String industry) {
		this.industry = industry;
	}

	public String getCorp_name() {
		return corp_name;
	}

	public void setCorp_name(String corp_name) {
		this.corp_name = corp_name;
	}

	public String getCorp_short() {
		return corp_short;
	}

	public void setCorp_short(String corp_short) {
		this.corp_short = corp_short;
	}

	public String getIstag() {
		return istag;
	}

	public void setIstag(String istag) {
		this.istag = istag;
	}

	public Date getCreate_date() {
		return create_date;
	}

	public void setCreate_date(Date create_date) {
		this.create_date = create_date;
	}

	public Date getModify_date() {
		return modify_date;
	}

	public void setModify_date(Date modify_date) {
		this.modify_date = modify_date;
	}



}
